package one_stage.day03;

public class ForDemo01 {
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
        System.out.println("**************");
        for (int j=5;j>0;j--){
            System.out.print(j);
            System.out.print(" ");
        }
    }
}
